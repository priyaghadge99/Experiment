public class Pattern3 {
    public static void main(String[] args) {
        int row=3;
        int val=1;
        for(int i=1;i<=row;i++){
            for(int j=1;j<=i;++j)
            {
                System.out.print(val);
                val++;
            }
            System.out.println("");
        }

    }
    }


